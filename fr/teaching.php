				<li><b>Innovation Master</b> - <u>Master</u>.<br>L'objectif du défi proposé dans le contexte de cet UE Innovation en Master est d'étudier les différentes solutions théoriques et logicielles pour la conception d'un ''provider'' de mot de passes. Les modèles IA employés tournent autour des réseuax de neurones récurrents.</li>
				<li><b>Projets Master</b> - <u>Master</u>.<br>Les étudiants ont pour objectif de mener à bien un projet informatique de la conception via un cahier des charges lors ud premier semestre, à la réalisation à proprement dit au second semestre. Ainsi, les étudiants doivent respecter les bonnes pratiques liées à la conduite de projet (organisation du travail, suivi, réalisation de comptes rendus, organisation de réunions de travail, etc.). J'ai donc en charge d'assurer la bonne tenue de ces projets, l'organisation des évaluations des cahiers des charges et des présentations, l'envoi des comptes rendus en temps et en heure, l'enregistrement des sujets et des groupes, la médiation entre les étudiants et les tuteurs, etc.</li>
                <li><b>Interface Homme-Machine (IHM)</b> - <u>Licence</u>.<br>L'objectif de ce cours est de revoir tous les éléments de base de l'orienté objets, leur traduction en C# ainsi que les concepts spécifiques au langage. L'accent est mis particulièrement sur les délégués et les événements, afin d'introduire leur utilisation dans les formulaires. Enfin, la réalisation d'Interfaces Homme-Machine sera proposée en utilisant directement les classes du Framework .NET, et mise en œuvre sur des travaux pratiques.</li>
                <li><b>Projet réseaux</b> - <u>Licence</u>.<br>Ce nouveau cours se trouve à l'interface entre le génie logiciel et les applications réseaux. L'objectif est de r&eacute;aliser une interface Web semblable &agrave; celles des fournisseurs d'acc&eacute;s (Box) en PHP pour la gestion de scripts de configuration r&eacute;seaux (DHCP, DNS, WebMail, Filtrage...).</li>
                <li><b>Conception et Modélisation Orientée Objet</b> - <u>Master</u>.<br>Cet enseignement a pour objectif de  fournir aux &eacute;tudiants la ma&icirc;trise de la conception du logiciel &agrave; l'aide d'outils et concepts li&eacute;s &agrave; la programmation orient&eacute;e objet et du langage de Java.</li>
                <li><b>Architectures Distribut&eacute;es</b> - <u>Master</u>.<br>Diff&eacute;rentes notions liées aux architectures distribuées sont étudiées : Middlewrae (Ice), Services Web (SOAP, WSDL, ...) ainsi que différentes technologies : EJB, Applet/Servlet Java. Le projet met en exergue différents langages de programmation et met l'accent &eacute;galement sur leur interpolarité dans une application portable android.</li>
